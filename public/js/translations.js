// Language Management System
class LanguageManager {
    constructor() {
        this.currentLanguage = 'en';
        this.supportedLanguages = ['en', 'hi', 'mr'];
        this.init();
    }
    
    init() {
        // Load saved language preference
        const savedLang = localStorage.getItem('krishimitra_language');
        if (savedLang && this.supportedLanguages.includes(savedLang)) {
            this.currentLanguage = savedLang;
        }
        
        // Apply initial translation
        this.applyTranslations();
    }
    
    setLanguage(langCode) {
        if (!this.supportedLanguages.includes(langCode)) {
            console.error('Unsupported language:', langCode);
            return;
        }
        
        this.currentLanguage = langCode;
        localStorage.setItem('krishimitra_language', langCode);
        this.applyTranslations();
        
        // Dispatch language change event
        window.dispatchEvent(new CustomEvent('languageChanged', { 
            detail: { language: langCode } 
        }));
    }
    
    translate(key) {
        const keys = key.split('.');
        let value = translations[this.currentLanguage];
        
        for (const k of keys) {
            if (value && typeof value === 'object') {
                value = value[k];
            } else {
                // Fallback to English if translation not found
                value = translations.en;
                for (const fallbackKey of keys) {
                    if (value && typeof value === 'object') {
                        value = value[fallbackKey];
                    } else {
                        return key; // Return key if no translation found
                    }
                }
                break;
            }
        }
        
        return value || key;
    }
    
    applyTranslations() {
        // Translate all elements with data-translate attribute
        const elementsToTranslate = document.querySelectorAll('[data-translate]');
        
        elementsToTranslate.forEach(element => {
            const key = element.getAttribute('data-translate');
            
            // Store original text if not already stored
            if (!element.hasAttribute('data-original-text')) {
                if (element.tagName === 'INPUT' && element.type !== 'button') {
                    element.setAttribute('data-original-text', element.placeholder || '');
                } else if (element.tagName === 'INPUT' && element.type === 'button') {
                    element.setAttribute('data-original-text', element.value || '');
                } else {
                    element.setAttribute('data-original-text', element.textContent || '');
                }
            }
            
            const translation = this.translate(key);
            const originalText = element.getAttribute('data-original-text');
            
            // Use translation if found, otherwise use original text
            const textToUse = (translation === key) ? originalText : translation;
            
            if (element.tagName === 'INPUT' && element.type !== 'button') {
                element.placeholder = textToUse;
            } else if (element.tagName === 'INPUT' && element.type === 'button') {
                element.value = textToUse;
            } else {
                element.textContent = textToUse;
            }
        });
        
        // Update language selector
        const languageSelectors = document.querySelectorAll('.language-selector');
        languageSelectors.forEach(selector => {
            selector.value = this.currentLanguage;
        });
        
        // Update page direction for RTL languages (if needed in future)
        document.documentElement.dir = 'ltr'; // All our languages are LTR
    }
    
    getCurrentLanguage() {
        return this.currentLanguage;
    }
    
    getSupportedLanguages() {
        return this.supportedLanguages;
    }
}

// Create global language manager instance
window.languageManager = new LanguageManager();

// Utility function for quick translation
window.t = function(key) {
    return window.languageManager.translate(key);
};

